void main() {
  //all operations which are apply on arithmetic operators is also apply on unary operators
  double k = 5.67;
  print("k : $k");
  print("k++ : ${k++}");
  print("k : $k");
  print("k-- : ${k--}");
  print("k : $k");
  print("++k : ${++k}");
  print("k : $k");
  print("--k : ${--k}");
  print("k : $k");

}