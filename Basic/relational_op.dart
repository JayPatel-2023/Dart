void main() {
  //relational operators
  double l = 5.67;
  double m = 7;
  print("equal == : ${l == m}");
  print("not equal != : ${l != m}");
  //all operations which are apply on arithmetic operators is also apply on unary operators
  print("greater than > : ${l > m}");
  print("less than < : ${(l < m)}");
  print("greater than or equal >= : ${l >= m}");
  print("less than or equal <= : ${l <= m}");

}